    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
      // Get the selected row index
    int selectedRow = jTable1.getSelectedRow();
    
    // Check if any row is selected
    if (selectedRow != -1) {
        // Get the plate number from the selected row
        String plateNo = jTable1.getValueAt(selectedRow, 0).toString();
        
        // Confirmation dialog before deleting
        int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this record?", "Confirmation", JOptionPane.YES_NO_OPTION);
        
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                // Delete the record from the database
                String query = "DELETE FROM lto WHERE TRUCKPLATENUMBER=?";
                pstmt = conn.prepareStatement(query);
                pstmt.setString(1, plateNo);
                pstmt.executeUpdate();
                
                // Remove the row from the table
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.removeRow(selectedRow);
            } catch (SQLException ex) {
                ex.printStackTrace();
                // Handle any SQL exception here
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "Please select a record to delete.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_DeleteActionPerformed


    private void loadData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        try {
            Statement stmt = conn.createStatement();
           ResultSet rs = stmt.executeQuery("SELECT * FROM lto"); 
            while (rs.next()) {
                String plateNo = rs.getString("TRUCKPLATENUMBER");
                Date date = rs.getDate("DATE");
                model.addRow(new Object[]{plateNo, date});
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    // Method to insert data into the database
    private void insertData(String plateNo, Date date) {
        try {
            pstmt = conn.prepareStatement("INSERT INTO lto (TRUCKPLATENUMBER, DATE) VALUES (?, ?)");
            pstmt.setString(1, plateNo);
            pstmt.setDate(2, (java.sql.Date) date);
            pstmt.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
    
    // Method to update data in the database
    private void updateData(String plateNo, Date date) {
        try {
             int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to register this truck?", "Confirmation", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) 
            pstmt = conn.prepareStatement("UPDATE lto SET DATE = ? WHERE TRUCKPLATENUMBER = ?");
            pstmt.setDate(1, (java.sql.Date) date);
            pstmt.setString(2, plateNo);
            pstmt.executeUpdate();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }
